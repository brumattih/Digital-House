// Crie uma função que receba uma string em minúsculas, converta-a em maiúsculas e as retorne. 
// Investigue o que o método de .toUpperCase() faz.

function converteStringParaMaiuscula(stringMinuscula) {
    return stringMinuscula.toUpperCase()
}

resultado = converteStringParaMaiuscula("fazendo um teste")
